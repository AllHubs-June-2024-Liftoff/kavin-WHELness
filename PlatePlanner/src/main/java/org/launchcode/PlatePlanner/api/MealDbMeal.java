    public Recipe convertToRecipe() {
        Recipe recipe = new Recipe();

        recipe.setName(this.strMeal);
        /* Currently there is no direct equivalent field in MealDB for description */
        //recipe.setDescription();
        recipe.setImageURL(this.strMealThumb);
        recipe.setInstructions(this.strInstructions);
        recipe.setSourceURL("https://www.themealdb.com/api/json/v1/1/lookup.php?i="+this.idMeal);
        /* Convert strIngredient1 through strIngredient20 and strMeasure1 through strMeasure20 to
        RecipeIngredients made up of (Ingredient ingredient, double quantity, String unit)
        strMeasure examples include:
        strMeasure1: "1 pound",
        strMeasure2: "1/4 cup",
        strMeasure3: "3 cloves",
        strMeasure4: "1 tin ",
        strMeasure5: "1/2 teaspoon",
        strMeasure6: "1/2 teaspoon",
        strMeasure7: "6 leaves",
        strMeasure8: "sprinkling",
        strMeasure1: "3/4 cup",
        strMeasure2: "1/2 cup",
        strMeasure3: "1/4 cup",
        strMeasure4: "1/2 teaspoon",
        strMeasure5: "1/2 teaspoon",
        strMeasure6: "4 Tablespoons",
        strMeasure7: "2",
        strMeasure8: "1 (12 oz.)",
        strMeasure9: "3 cups",
         */
        if (this.strIngredient1 != null) {
            List<RecipeIngredient> recipeIngredients = new ArrayList<RecipeIngredient>();
            Double quantity = 0.0;
            String unit = "";
            String ingredient = "";
            String measure = "";
            String ingPrefix = "getStrIngredient";
            String ingMethod = "";
            String measPrefix = "getStrMeasure";
            String measMethod = "";
            for (int i = 1; i <= 20; i++) {
                ingMethod = ingPrefix + i;
                measMethod = measPrefix + i;
                try {
                    Method ingredientMethod = MealDbMeal.class.getMethod(ingMethod);
                    Method measureMethod = MealDbMeal.class.getMethod(measMethod);
                    ingredient = (String)ingredientMethod.invoke(this);
                    if (ingredient != null && ingredient.length() > 0) {
                        measure = (String)measureMethod.invoke(this);
                        quantity = 0.0;
                        unit = "";
                        if (measure != null && measure.length() > 0) {
                            String[] measurePieces = measure.split(" ");
                            if (measurePieces.length > 0) {
                                try {
                                    quantity = parseQuantity(measurePieces[0]);
                                    if (measurePieces.length > 1) {
                                        unit = measurePieces[1];
                                        for (int j = 2; j < measurePieces.length; j++) {
                                            unit = unit + " " + measurePieces[j];
                                        }
                                    }
                                } catch (NumberFormatException e) {
                                    quantity = 1.0;
                                    unit = String.join(" ", measurePieces);
                                }
                            }
                        }
                        recipeIngredients.add(new RecipeIngredient(recipe, new Ingredient(ingredient), quantity, unit));
                    }
                } catch (Exception e) {
                    System.out.println("Exception: " + e.toString());
                    //logger.log("Trouble calling ingredient methods in MealDbMeal.");
                }
            }
            if (!recipeIngredients.isEmpty()) {
                recipe.setRecipeIngredients(new HashSet<>(recipeIngredients));
            }
        }

        if (this.strTags != null) {
            Tag[] tags = Arrays.stream(this.strTags.split(",")).map(item -> new Tag(item)).toArray(Tag[]::new);
            recipe.setTags(new HashSet<>(Arrays.asList(tags)));
        }

        return recipe;
    }

    /* Parses a string for a number in the form of a fraction or decimal.
    Throws NumberFormatException if quantity is not a number.
     */
    private static Double parseQuantity (String quantity) {
        if (quantity.contains("/")) {
            String[] qty = quantity.split("/");
            return Double.parseDouble(qty[0]) / Double.parseDouble(qty[1]);
        } else {
            return Double.parseDouble(quantity);
        }
    }

